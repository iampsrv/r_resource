# Arithmetic operators
x <- 12
y <- 5

# Addition
result <- x + y
cat("Addition:", result, "\n")

# Subtraction
result <- x - y
cat("Subtraction:", result, "\n")

# Multiplication
result <- x * y
cat("Multiplication:", result, "\n")

# Division
result <- x / y
cat("Division:", result, "\n")

# Exponentiation
result <- x ^ y
cat("Exponentiation:", result, "\n")

# Integer division
result <- x %/% y
cat("Integer division:", result, "\n")

# Modulo
result <- x %% y
cat("Modulo:", result, "\n")
